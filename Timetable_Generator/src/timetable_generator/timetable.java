package timetable_generator;

public class timetable {

	public int cid;
	public int cr_id;
	public int school_id;
	public int day;
	public int slot;
	
	public timetable(int a,int b,int c,int d,int e){
		this.cid=a;
		this.cr_id=b;
		this.school_id=c;
		this.day=d;
		this.slot=e;
		
	}
}
